namespace Clockmaker0.Data;

/// <summary>
/// Enum representing the possible theme variants
/// </summary>
public enum ThemeEnum
{
    /// <summary>
    /// Match the system theme
    /// </summary>
    Auto = 0,
    /// <summary>
    /// Force Dark Mode
    /// </summary>
    Dark = 1,
    /// <summary>
    /// Force Light Mode
    /// </summary>
    Light = 2,
}